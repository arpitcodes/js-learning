const originalString = "hey there! today we will be learning string in js"
const lowerCaseString = originalString.toLowerCase();
const upperCaseString = originalString.toUpperCase();

console.log(`original string: ${originalString}`);
console.log(`upper case string: ${upperCaseString}`);
console.log(`lower case string: ${lowerCaseString}`);
console.log(`Length of the string: ${originalString.length}`)
console.log(`substring calculation from the string: ${originalString.substring(5,10)}`)


